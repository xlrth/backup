#include "CSize.h"

////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////
CSize::CSize(long long value)
    :
    mValue(value)
{}

////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////
CSize::CSize(std::uintmax_t value)
    :
    mValue(static_cast<long long>(value))
{}

////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////
CSize::operator long long() const
{
    return mValue;
}

////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////
bool CSize::IsSpecified() const
{
    return mValue != UNSPECIFIED;
}
